#include<stdio.h>
int main(){
    int a=10,b=20;
    printf("Equal operator (==) : %d\n", a==b);  //result will be shown in 0 false or 1 true
    printf("Not Equal operator (!=) : %d\n", a!=b);
    printf("Greater then operator (>) : %d\n", a>b);
    printf("Less then operator (<) : %d\n", a<b);
    printf("Greater then or equal (>=) : %d\n", a>=b);
    printf("Less then or equal (<=) : %d\n", a<=b);
}
